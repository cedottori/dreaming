
###   relations between mythos, logos and psyche ###


This is a dense intersection. The three terms have distinct genealogies that converge in ways that shaped Western psychology and philosophy of mind.

---

### 1. Psyche in the Archaic Period

In Homer, *psyche* (*ψυχή*) designates the **breath-soul** that leaves the body at death — a shade (*eidolon*) without cognitive function. Thought, emotion, and perception are distributed across other terms: *thumos* (passionate impulse), *nous* (perception/intellect), *phrenes* (diaphragm, seat of feeling). There is no unified psychological subject in Homer.

**Reference:** Snell, B. (1953). *The Discovery of the Mind: The Greek Origins of European Thought.* Harvard University Press.

---

### 2. Logos and Its Ambiguity

*Logos* (*λόγος*) carries an irreducible semantic range: word, reason, proportion, account, discourse, relation. This polysemy is not a deficiency — it reflects the Greek intuition that **rational structure and its linguistic expression are inseparable**.

Heraclitus (c. 500 BCE) is the pivotal figure: the *Logos* is a universal principle of proportion and opposition that most people fail to perceive despite living in constant contact with it. Crucially, Heraclitus connects *logos* to the *psyche*:

> "You could not discover the limits of the soul (*psyche*), even if you traveled every road — so deep is its *logos*." (Fr. 45, DK)

This is the first explicit linkage: *psyche* has depth, and that depth is *logos* — not irrational, but inexhaustibly rational.

**Reference:** Kahn, C. H. (1979). *The Art and Thought of Heraclitus.* Cambridge University Press.

---

### 3. Plato: The Tripartite Soul and the Problem of Myth

Plato restructures all three terms simultaneously. In the *Republic* (IV, 435–441), *psyche* becomes tripartite:

- *Logistikon* — the reasoning part, aligned with *logos*
- *Thumoeides* — the spirited part
- *Epithumetikon* — the appetitive part

The political and psychological orders mirror each other: justice in the city and in the soul consists in *logos* governing the other parts.

Yet Plato's treatment of myth (*mythos*) complicates this rational hierarchy. In the *Phaedrus* (245c–249d), the soul's nature and its fate cannot be demonstrated (*apodeixis*) but only approached through a likely myth (*eikôs mythos*). The soul is figured as a winged charioteer — an irreducibly imaginal representation of something that resists propositional capture. Myth is not opposed to psyche; it is the **appropriate medium for self-knowledge at the limits of logos**.

**Reference:** Plato. *Phaedrus*, 246a–249d; *Republic*, IV; *Timaeus*, 69c–72d.

---

### 4. Aristotle: Logos as the Soul's Proper Activity

For Aristotle, *psyche* is the **form of a living body** (*De Anima* II.1, 412a) — not a substance trapped in matter but the organizing principle of a natural body capable of life. This is a logos-saturated conception: the soul just is the rational structure (form, *eidos*) that makes a body be what it is.

Rational activity (*nous*) is the highest expression of psyche. In *De Anima* III.4–5, Aristotle distinguishes passive and active intellect (*nous pathetikos / nous poietikos*) — a passage of extraordinary obscurity that generated centuries of commentary and prefigures later debates about the unconscious and the collective mind.

Myth enters Aristotle's framework differently: in the *Poetics*, mythic narrative (*mythos* as plot) is the formal structure through which the soul (*psyche*) of drama is expressed. Narrative is the logos of action.

**Reference:** Aristotle. *De Anima*, II.1–2; III.4–5. *Poetics*, VI (1450a).

---

### 5. Stoics: Logos as Cosmic Psyche

The Stoics identified *logos* with the rational fire (*pneuma*) that structures the cosmos — a World-Soul in which individual psyches participate. This is perhaps the most radical integration of the three terms: *psyche* is a fragment of cosmic *logos*, and mythological figures are allegorical expressions of *logos*'s various functions in nature and mind.

**Reference:** Long, A. A. & Sedley, D. N. (1987). *The Hellenistic Philosophers*, Vol. 1. Cambridge University Press.

---

### 6. Plotinus and Neoplatonism: Psyche as Mediator

In Plotinus (*Enneads*, III–IV), *psyche* occupies the **middle hypostasis** between *Nous* (Intellect) and matter. It is the principle that both descends into the world and ascends toward the One. Logos here becomes the generative rational principle (*logos spermatikos*) through which Soul produces the material world.

Myth in Neoplatonism — particularly in Porphyry and Proclus — is interpreted as **theurgic symbol**: not allegory reducible to rational content, but a participatory structure that enacts the soul's ascent. Myth functions where discursive logos cannot reach.

**Reference:** Plotinus. *Enneads*, IV.3–4 (On the Soul). Proclus. *Commentary on the Republic*, essay VI.

---

### 7. Modern Depth Psychology: The Triangle Reconfigured

Jung explicitly reorganized these three terms. *Logos* becomes associated with the **masculine principle** (directed thinking, differentiation), *Eros* with the feminine (relation, feeling) — a move that has been critically examined for its gender essentialism. *Psyche* expands to include the **unconscious**, which myth expresses through the language of archetypes.

For Jung, mythological images are the *logos* of the unconscious psyche — autonomous structural forms that thought cannot generate but can only receive and interpret.

Hillman's archetypal psychology (1975) radicalizes this: the psyche is inherently **imaginal**, and logos-thinking (causal, reductive analysis) is only one of many modes the soul employs. Dreams and myths are not pre-logical but **differently logical** — they have their own necessity and coherence.

**References:**
- Jung, C. G. (1921). *Psychological Types*, CW 6. — On Logos and Eros.
- Hillman, J. (1975). *Re-Visioning Psychology.* Harper & Row.
- Hillman, J. (1979). *The Dream and the Underworld.* Harper & Row.

---

### 8. Vernant and Structuralism: A Non-Psychological Reading

Vernant cautions against projecting modern psychological categories onto Greek materials. For him, *psyche*, *logos*, and *mythos* are embedded in specific social and discursive practices — the Greek polis, the agora, oral tradition — and cannot be abstracted into a timeless psychology without distortion.

**Reference:** Vernant, J.-P. & Vidal-Naquet, P. (1988). *Myth and Tragedy in Ancient Greece.* Zone Books.

---

### Synthesis

The three terms form a mobile triangle rather than a fixed hierarchy:

- *Logos* provides structure, proportion, and discursive accountability
- *Mythos* provides narrative form for what resists direct formulation
- *Psyche* is the field in which both operate — and whose depth neither fully exhausts

The recurring historical pattern is that *logos* attempts to subordinate *mythos*, discovers it cannot fully do so, and resorts to myth at its own limits — which is itself evidence that *psyche* exceeds both.


